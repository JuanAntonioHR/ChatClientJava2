    //Abrir ventana de chat privado al seleccionar un usuario de usersList
    private void usersListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usersListMouseClicked
        if (evt.getClickCount() == 2) {
            String ipcliente = usersList.getSelectedValue();
            String username = ipcliente.substring(0, ipcliente.indexOf("@"));

            // Verificar si ya existe una ventana de chat privado para este usuario
            if (!privateChatWindows.containsKey(username)) {
                PrivateChat privateChatW = new PrivateChat(cliente, ipcliente);
                privateChatW.setVisible(true);
                privateChatWindows.put(username, privateChatW);
                privateChatW.getMessageTF().requestFocus(); // getMessageTF() es un m√©todo de acceso a messageTF en la clase PrivateChat

                MensajePrivadoHandler mensajePrivadoHandler = new MensajePrivadoHandler(cliente, privateChatW);
                Thread hiloMensajesPrivados = new Thread(mensajePrivadoHandler);
                hiloMensajesPrivados.start();
            } else {
                // La ventana de chat privado ya est√° abierta para este usuario, puedes realizar alguna acci√≥n aqu√≠ si es necesario.
            }
        }
    }//GEN-LAST:event_usersListMouseClicked

    public void mostrarMensajes() throws IOException {
        String line = cliente.recibirMensaje();
        if (line != null){
            String chatP = line + "\n";
            chatTextArea.append(chatP);
        }
    }

    public void mostrarUsuarios() {
        usersList.setListData(cliente.getUsuarios());
    }
    
    private void enviarMensaje() {
        String mensaje = messageTextField.getText();
        cliente.enviarMensaje(mensaje);
        messageTextField.setText("");  // Limpiar el campo de texto despu√©s de enviar el mensaje
        
        // Mostrar mensaje enviado en el chat
        String chatP = cliente.getUsername() + ": " + mensaje + "\n";
        chatTextArea.append(chatP);
    }

